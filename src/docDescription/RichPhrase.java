public class RichPhrase {
    
    private String phraseWords;
    private String phraseDef;
    private String phraseRelatedSyno;
    private String phraseExactSyno;
    
    

    public String getPhraseWords() {
        return phraseWords;
    }

    public void setPhraseWords(String phraseWords) {
        this.phraseWords = phraseWords;
    }

    public String getPhraseDef() {
        return phraseDef;
    }

    public void setPhraseDef(String phraseDef) {
        this.phraseDef = phraseDef;
    }

    public String getPhraseRelatedSyno() {
        return phraseRelatedSyno;
    }

    public void setPhraseRelatedSyno(String phraseRelatedSyno) {
        this.phraseRelatedSyno = phraseRelatedSyno;
    }

    public String getPhraseExactSyno() {
        return phraseExactSyno;
    }

    public void setPhraseExactSyno(String phraseExactSyno) {
        this.phraseExactSyno = phraseExactSyno;
    }
    
    
}
